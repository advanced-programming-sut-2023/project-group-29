package model;

public class Building
{
    private int cost;
    private int hp;
    private int workerCount;
    public int getCost()
    {
        return cost;
    }

    public void setCost(int cost)
    {
        this.cost = cost;
    }

    public int getHp()
    {
        return hp;
    }

    public void setHp(int hp)
    {
        this.hp = hp;
    }

    public int getWorkerCount()
    {
        return workerCount;
    }

    public void setWorkerCount(int workerCount)
    {
        this.workerCount = workerCount;
    }
}
